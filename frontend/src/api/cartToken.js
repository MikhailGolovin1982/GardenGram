// Хранение токена гостевой корзины.
//
// Гость опознаётся бэкендом по заголовку X-Cart-Token (UUID). Этот токен бэкенд
// присылает В ТЕЛЕ ответа (поле token) при первом добавлении товара. Чтобы «та же»
// корзина узнавалась между запросами и сессиями, мы храним токен у себя и шлём его
// в каждом запросе к корзине.
//
// Почему localStorage: переживает перезагрузку и закрытие вкладки («не теряем между
// сессиями»). Cookie сознательно не используем — проект API-first на JWT.

const KEY = 'gg_cart_token'

// Прочитать сохранённый токен или null (новый гость / приватное окно).
export function getCartToken() {
  return localStorage.getItem(KEY) || null
}

// Сохранить токен. Пустые значения игнорируем: пустое представление корзины
// приходит с token:null — таким ответом НЕ должны затирать уже сохранённый токен.
export function setCartToken(token) {
  if (token) {
    localStorage.setItem(KEY, token)
  }
}

// Удалить токен. Пригодится позже (выход из аккаунта / merge гостевой корзины);
// сейчас держим на всякий случай, в потоке Шага 4 не зовётся.
export function clearCartToken() {
  localStorage.removeItem(KEY)
}
